// Helper function to calculate the area of a rectangle
function calculateRectangleArea(width, height) {
    return width * height;
  }
  
  // Main code to use the helper function
  const width = 5; // You can change the width of the rectangle
  const height = 8; // You can change the height of the rectangle
  const area = calculateRectangleArea(width, height);
  console.log(`The area of the rectangle with width ${width} and height ${height} is ${area}`);
  